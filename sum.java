public class Sum{
  //attributes-- AKA variables
  private int num1;
  private int num2;

 // Sum constructor
 public Sum(int number1, int number2){
   num1 = number1;
   num2 = number2;
 }
 //behavior
 //my sumOf method will be adding both variables
  public int sumOf(){
   return num1 + num2;
    
  }
}